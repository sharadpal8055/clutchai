import { getAuthClient } from "../services/calendar/calendar.service.js";

import db from "../config/firebase.js";
import { getUserCalendar,  deleteExistingEvents, } from "../services/calendar/calendar.service.js";
export const connectCalendar = async (req, res) => {
  try {
    const oauth2Client = getAuthClient();

    const { userId } = req.query;

    const url = oauth2Client.generateAuthUrl({
      access_type: "offline",
      prompt: "consent",
      scope: ["https://www.googleapis.com/auth/calendar"],
      state: userId,
    });
    console.log("calendar callback", process.env.GOOGLE_REDIRECT_URI);
    res.json({
      success: true,
      url,
    });
  } catch (error) {
    res.status(500).json({
      success: false,
      message: error.message,
    });
  }
};

export const oauthCallback = async (req, res) => {
  try {
    const { code, state } = req.query;

    const oauth2Client = getAuthClient();

    const { tokens } = await oauth2Client.getToken(code);
    console.log("State:", state);
    console.log("Refresh Token:", tokens.refresh_token);
    await db
      .collection("users")
      .doc(state)
      .set(
        {
          calendar: {
            connected: true,
            refreshToken: tokens.refresh_token,
          },
        },
        { merge: true },
      );
    console.log("Calendar saved successfully");
    res.send(`
      <h2>✅ Google Calendar Connected</h2>
      <p>You can close this window and return to Clutch AI.</p>
    `);
  } catch (error) {
    console.error(error);

    res.status(500).json({
      success: false,
      message: error.message,
    });
  }
};

export const syncTaskToCalendar = async (req, res) => {
  try {
    const { taskId } = req.params;

    const taskDoc = await db.collection("tasks").doc(taskId).get();

    if (!taskDoc.exists) {
      return res.status(404).json({
        success: false,
        message: "Task not found",
      });
    }

    const task = taskDoc.data();

    const calendar = await getUserCalendar(task.userId);
    if (task.calendarSync?.synced && task.calendarSync?.eventIds?.length) {
      await deleteExistingEvents(
        calendar,

        task.calendarSync.eventIds,
      );
    }

    // Temporary event (we'll improve this in the next step)
    const createdEvents = [];

    const schedule = task.planner?.schedule || [];

    for (const item of schedule) {
      const baseDate = new Date(task.deadline);

      // If planner says Today, schedule one day before deadline.
      if (item.day === "Today") {
        baseDate.setDate(baseDate.getDate() - 1);
      }

      const [startHour, startMinute] = item.start.split(":");
      const [endHour, endMinute] = item.end.split(":");

      const startDate = new Date(baseDate);
      startDate.setHours(Number(startHour), Number(startMinute), 0, 0);

      const endDate = new Date(baseDate);
      endDate.setHours(Number(endHour), Number(endMinute), 0, 0);

      const event = {
        summary: item.task,

        description: `${task.title}\n\nGenerated by Clutch AI`,

        start: {
          dateTime: startDate.toISOString(),
          timeZone: "Asia/Kolkata",
        },

        end: {
          dateTime: endDate.toISOString(),
          timeZone: "Asia/Kolkata",
        },

        colorId: "9",
      };

      const response = await calendar.events.insert({
        calendarId: "primary",

        requestBody: event,
      });

      createdEvents.push({
        id: response.data.id,
        link: response.data.htmlLink,
      });
    }
    await db
      .collection("tasks")
      .doc(taskId)
      .update({
        calendarSync: {
          synced: true,

          syncedAt: new Date(),

          eventIds: createdEvents.map((event) => event.id),
        },
      });
    res.json({
      success: true,

      totalEvents: createdEvents.length,

      events: createdEvents,
    });
  } catch (error) {
    console.error(error);

    res.status(500).json({
      success: false,
      message: error.message,
    });
  }
};
